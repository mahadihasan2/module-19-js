var color = 'red';
switch(color){
    case "blue":
       console.log('this color is blue')
    break;

    case "yellow":
        console.log('this color is yellow')
        break;
    case "green":
        console.log('this color is green')
        break;
    case "white":
        console.log("this color is white")
        break;
    case "red":
        console.log("this color is red")
        break;
    default :
     console.log("this color is black") 
}